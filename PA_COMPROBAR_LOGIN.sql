CREATE PROCEDURE [dbo].[PA_COMPROBAR_LOGIN]
(  
	@EMAIL VARCHAR(100),
	@PASS VARCHAR(100),

  
 @MENSAJE VARCHAR(100) OUTPUT,  
 @RETCODE INT OUTPUT  
)  
AS  
  
BEGIN TRY  
   

   SET @RETCODE = 0
   SET @MENSAJE = ''
   
 IF ISNULL(@EMAIL,'') = ''  
 BEGIN  
  SET  @RETCODE = 10  
  SET @MENSAJE = 'El parametro email no puede ser vacío'  
  RETURN  
 END  

 IF EXISTS(SELECT @EMAIL, @PASS FROM USUARIOS)
 BEGIN
	SET @RETCODE = 1
	SET @MENSAJE = 'Los datos existesn'
	RETURN 
 END 
  
  
 INSERT INTO USUARIOS  
 (  
	EMAIL,
	PASS
 )  
 VALUES  
 (  
	@EMAIL,
	@PASS
	 
 )  
   
 RETURN   
  
END TRY  
  
BEGIN CATCH  
  
END CATCH